package com.dao;

public class ItemList {
	
	  int itemCode ;
      String itemName ;
      double itemPrice ;
      int itemQuantity ;
	public ItemList(int itemCode, String itemName, double itemPrice, int itemQuantity) {
		super();
		this.itemCode = itemCode;
		this.itemName = itemName;
		this.itemPrice = itemPrice;
		this.itemQuantity = itemQuantity;
	}
      

}
